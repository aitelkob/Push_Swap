#include "../push_swap.h"

int	ft_isnum(char *str)
{
	int	i;

	i = 0;
	if (str[0] == '-')
		str++;
	while (str[i] != '\0')
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}
